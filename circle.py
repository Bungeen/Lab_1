import math


def area(r):
    '''Принимает число r, возвращает квадрат числа r 
    помноженный на число Пи из библиотеки math: площадь круга'''
    return math.pi * r * r


def perimeter(r):
    '''Принимает число r, возвращает удвоенное 
    произведение чила r на Пи из библиотеки math: периметр круга'''
    return 2 * math.pi * r

